    private void startConnection() {
        try {
            clientSocket = new Socket("localhost", 1991);
            dosCS = new DataOutputStream(clientSocket.getOutputStream());
            disCS = new DataInputStream(clientSocket.getInputStream());

            t1 = new Thread(new ClientPriceObserver(this, disCS));
            t1.start();
        } catch (IOException ex) {
            System.out.println(ex.toString());
        }
    }

    private void getTotalPrice() {
        try {
            dosCS.writeInt(GET_CHECKOUT_REQ);
            dosCS.writeInt((Integer) appleQty.getValue());
            dosCS.writeInt((Integer) bananaQty.getValue());
            dosCS.writeInt((Integer) orangeQty.getValue());

//            dosCS.close();
//            disCS.close();
//            clientSocket.close();
        } catch (IOException ex) {
            System.out.println(ex.toString());
        }
    }

